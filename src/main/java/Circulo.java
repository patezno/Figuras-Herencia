public class Circulo {

    // Atributos

    private int radio = 0;
    private String nombre = null;

    // Constructores

    public Circulo() {
    }

    public Circulo(int radio) {
        this.radio = radio;
    }

    public Circulo(String nombre, int radio) {
        this.nombre = nombre;
        this.radio = radio;
    }

}
